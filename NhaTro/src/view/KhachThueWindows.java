    public void fillToGrid() {
        model = (DefaultTableModel) tblKhachThue.getModel();
        model.setRowCount(0);
        String name = txtTimKiem.getText();
        List<KhachThue> list = dao.selectByName(name);
        for (KhachThue kh : list) {
            Object[] row = new Object[]{kh.getIdKhachThue(), kh.getHoTen(), kh.getNgaySinh(), kh.isGioiTinh() == true ? "Nam" : "Nữ",
                kh.getQueQuan(), kh.getDienThoai(), kh.getEmail(), kh.getcMND(), kh.getGhiChu()};
            model.addRow(row);
        }
    }
    TaiKhoanDao tkDao = new TaiKhoanDao();

    private void insert() {
        KhachThue objKhachThue = getModel();
        try {
            if (check()) {
                return;
            }
            if(txtTaiKhoan.getText().equals("") || txtMatKhau.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Nhập tài khoản, mật khẩu");
                return;
            }
            dao.insert(objKhachThue);
            this.fillToGrid();
            tkDao.insert(new TaiKhoan(txtTaiKhoan.getText(), txtMatKhau.getText(), "", Integer.parseInt(tblKhachThue.getValueAt(tblKhachThue.getRowCount() - 1, 0) + ""), false));
            JOptionPane.showMessageDialog(this, "Thêm thành công!");
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Thêm thất bại!");
        }
    }

    private void delete() {
        if (JOptionPane.showConfirmDialog(this, "Bạn thực sự muốn xóa?") == 0) {
            try {
                tkDao.delete(id);
                dao.delete(id);
                this.fillToGrid();
                this.clear();
                JOptionPane.showMessageDialog(this, "Xóa thành công!");
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(this, "Xóa thất bại!");
            }
        }
    }

    private void update() {
        KhachThue objKhachThue = getModel();
        try {
            if (check()) {
                return;
            }
            dao.update(objKhachThue);
            this.fillToGrid();
            JOptionPane.showMessageDialog(this, "Cập nhật thành công!");
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại!");
        }

    }

    private KhachThue getModel() {
        KhachThue objKhachThue = new KhachThue();
        try {
            objKhachThue.setHoTen(txtHoVaTen.getText());
            objKhachThue.setNgaySinh(DateHelper.toDate(txtNgaySinh.getText(), "dd/MM/yyyy"));
            objKhachThue.setGioiTinh(rdoNam.isSelected());
            objKhachThue.setQueQuan(txtQueQuan.getText());
            objKhachThue.setDienThoai(txtDienThoai.getText());
            objKhachThue.setEmail(txtEmail.getText());
            objKhachThue.setcMND(txtCMTND.getText());
            objKhachThue.setGhiChu(txtGhiChu.getText());
            objKhachThue.setIdKhachThue(id);
            return objKhachThue;
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ngày sinh phải đúng định dạng - yyyy/MM/dd");
            return null;
        }
    }

    private void clear() {
        KhachThue objKhachThue = new KhachThue();
        this.setModel(objKhachThue);
        setStatusButton(true);
    }

    private void setModel(KhachThue objKhachThue) {
        txtHoVaTen.setText(objKhachThue.getHoTen());
        txtNgaySinh.setText(String.valueOf(objKhachThue.getNgaySinh()));
        rdoNam.setSelected(objKhachThue.isGioiTinh());
        rdoNu.setSelected(!objKhachThue.isGioiTinh());
        txtQueQuan.setText(objKhachThue.getQueQuan());
        txtDienThoai.setText(objKhachThue.getDienThoai());
        txtEmail.setText(objKhachThue.getEmail());
        txtCMTND.setText(objKhachThue.getcMND());
        txtGhiChu.setText(objKhachThue.getGhiChu());
    }

    private void edit() {
        try {
            id = Integer.parseInt(tblKhachThue.getValueAt(this.index, 0) + "");
            KhachThue kt = dao.selectByID(id);
            if (kt != null) {
                this.setModel(kt);
                setStatusButton(false);
            }
        } catch (Exception e) {
            e.printStackTrace();
//            JOptionPane.showMessageDialog(this, "Lỗi truy vấn dữ liệu!");
        }
    }

    private boolean check() {
        String pattern = "\\w+@\\w+\\.\\w+";
        if (txtHoVaTen.getText().equals("") || txtNgaySinh.getText().equals("") || txtQueQuan.getText().equals("") || txtDienThoai.getText().equals("") || txtCMTND.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Yêu cầu nhập đầy đủ thông tin!");
            return true;
        }
        if (txtEmail.getText().length() > 0 && !txtEmail.getText().matches(pattern)) {
            JOptionPane.showMessageDialog(this, "Yêu cầu nhập đúng định dạng của email!");
            txtEmail.requestFocus();
            return true;
        }

        return false;
    }

    public void setStatusButton(Boolean status) {
        btnThem.setEnabled(status);
        btnCapNhat.setEnabled(!status);
        btnXoa.setEnabled(!status);
        pnlTaoTaiKhoan.setVisible(status);
        if (status == true) {
            pnlBtnThem.setBackground(colorEnable);
            pnlBtnCapNhat.setBackground(colorDisable);
            pnlBtnXoa.setBackground(colorDisable);
        } else {
            pnlBtnThem.setBackground(colorDisable);
            pnlBtnCapNhat.setBackground(colorEnable);
            pnlBtnXoa.setBackground(colorEnable);
        }
    }


